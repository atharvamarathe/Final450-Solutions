class Solution
{
public:
    vector<int> candyStore(int candies[], int N, int K)
    {
        sort(candies,candies+N);
        int min = 0;
        int count = 0;
        int i =0;
        while(count < N) {
            min += candies[i++];
            count += K+1;
        }
        int max = 0;
        int a = 1;
        while(i--) {
            max += candies[N-a];
            a++;
        }
        
        return {min,max};
        // Write Your Code here
    }
};

